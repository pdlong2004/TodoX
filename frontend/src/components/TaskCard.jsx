import React, { useState } from 'react'
import { Card } from './ui/card'
import { Button } from './ui/button'
import { Calendar, CheckCircle2, Circle, SquarePen, Trash2 } from 'lucide-react'
import { Input } from './ui/input'
import { cn } from "@/lib/utils"
import api from '@/lib/axois'
import { toast } from 'sonner'

const TaskCard = ({ task , index , handleTaskChange}) => {

    const [isEditting , setIsEditting] = useState(false)
    const [updateTaskTitle , setUpdateTaskTitle] = useState(`${task.title || ""}`)

    const deleteTask = async(taskId) => {
        try {
            await api.delete(`/tasks/${taskId}`)
            toast.success(`Nhiệm vụ ${task.title} đã được xóa`)
            handleTaskChange()
        } catch (error) {
            console.error("Lỗi khi xóa nhiệm vụ " , error)
            toast.error("Lỗi khi xóa nhiệm vụ")
        }
    }

    const updateTask = async() => {
        try {
            await api.put(`/tasks/${task._id}`, {
                title : updateTaskTitle,
            });
            toast.success(`Nhiệm vụ đã được đổi thành ${task.title}`)
            setIsEditting(false)
            handleTaskChange()
        } catch (error) {
            console.error("Lỗi khi cập nhật nhiệm vụ " , error)
            toast.error("Lỗi khi cập nhật nhiệm vụ")
        }
    }

    const toggleTaskCompleteButton = async () => {
        const isActive = task.status === "active"

        try {
            await api.put(`/tasks/${task._id}`, {
                status: isActive ? "complete" : "active",
                completedAt: isActive ? new Date().toISOString() : null
            })

            toast.success(
                isActive
                    ? `Nhiệm vụ "${task.title}" đã hoàn thành`
                    : `Nhiệm vụ "${task.title}" đã chuyển về đang làm`
            )

            handleTaskChange()
        } catch (error) {
            console.error("Lỗi khi cập nhật nhiệm vụ", error)
            toast.error("Lỗi khi cập nhật nhiệm vụ")
        }
    }


    const handleKeyPress = (e) => {
        if(e.key === "Enter"){
            updateTask()
        }
    }
  return (
    <Card className={cn(
        "px-4 bg-gradient-card shadow-custom-md hover:shadow-custom-lg transition-all duration-200 animate-fade-in group" ,
        task.status === 'complete' && 'opacity-75'
    )} 
    style={{ animationDelay : `${index * 50}ms` }}
    >
        <div className='flex item-center gap-4'>
            {/* nut tron */}
            <Button 
                variant='ghost'
                size='icon'
                className={cn(
                    "shrink-0 size-8 rounded-full transition-all duration-200",
                    task.status === 'complete' ? 'text-success hover:text-success/80' : 'text-muted-foreground hover:text-primary'
                )}
                onClick={toggleTaskCompleteButton}

            >
                {task.status === 'complete' ? (
                    <CheckCircle2 className='size-5' />
                ) : (
                    <Circle className='size-5' />
                )}
            </Button>

            {/* hien thi hoac chinh sua */}
            <div className='flex-1 min-w-0'>
                {isEditting ? (
                    <Input 
                     placeholder = 'Cần phải làm gì ?'
                     className="flex-1 h-12 text-base border-border/50 focus:border-primary/50 focus:ring-primary/20"
                     value={updateTaskTitle}
                     onChange={(e) => setUpdateTaskTitle(e.target.value)}
                     onKeyPress = {handleKeyPress}
                     onBlur={() => {
                        setIsEditting(false)
                        setUpdateTaskTitle(task.title || "")
                     }}
                    />
                ) : (
                    <p 
                        className={cn(
                            "text-base transition-all duration-200",
                            task.status === 'complete' ? 'line-through text-muted-foreground' : 'text-foreground'
                        )}
                    >
                        {task.title}
                    </p>
                )}

                {/* Ngay tao va ngay hoan thanh */}
                <div className='flex items-center gap-2 mt-1'>
                    <Calendar className='size-3 text-muted-foreground' />
                    <span className='text-xs text-muted-foreground '>
                        {new Date(task.createdAt).toLocaleString()}
                    </span>
                    {task.completedAt && (
                        <>
                            <span className='text-xs text-muted-foreground'> - </span>
                            <Calendar className='size-3 text-muted-foreground' />
                            <span className='text-xs text-muted-foreground'>
                                {new Date(task.completedAt).toLocaleString()}
                            </span>
                        </>
                    )}
                </div>
            </div>


            {/* nut chinh va xoa */}
            <div className='hidden gap-2 group-hover:inline-flex animate-slide-up'>
                {/* nut edit */}
                <Button
                    variant='ghost'
                    size='icon'
                    className='shrink-0 transition-color size-8 text-muted-foreground hover:text-info'
                    onClick = { () => {
                        setIsEditting(true)
                        setUpdateTaskTitle(task.title || "")
                    }}
                >
                    <SquarePen className='size-4'/>
                </Button>

                {/* nut xoa */}
                <Button 
                    variant='ghost'
                    size='icon'
                    className='shrink-0 transition-colors size-8 text-muted-foreground hover:text-destructive'
                    onClick={() => deleteTask(task._id)}
                >
                    <Trash2 className='size-4'/>
                </Button>

            </div>
        </div>
    </Card>
  )
}

export default TaskCard